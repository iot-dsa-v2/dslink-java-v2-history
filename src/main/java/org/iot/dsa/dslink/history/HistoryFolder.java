package org.iot.dsa.dslink.history;

import java.util.Collection;
import org.iot.dsa.node.DSInfo;

/**
 * Contains histories and other history folders.
 *
 * @author Aaron Hansen
 */
public class HistoryFolder extends AbstractHistoryNode {

    ///////////////////////////////////////////////////////////////////////////
    // Class Fields
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Instance Fields
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Constructors
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Public Methods
    ///////////////////////////////////////////////////////////////////////////

    @Override
    public DSInfo getDynamicAction(DSInfo target, String name) {
        DSInfo ret = null;
        if (target.get() == this) {
            switch (name) {
                case DELETE:
                    return actionInfo(DELETE, HistoryUtils.deleteNodeData);
                case FOLDER:
                    return actionInfo(FOLDER, HistoryUtils.newHistoryFolder);
                //case HISTORY:
                    //return actionInfo(HISTORY, HistoryUtils.newHistory);
            }
        }
        return ret;
    }

    @Override
    public void getDynamicActions(DSInfo target, Collection<String> names) {
        super.getDynamicActions(target, names);
        names.add(FOLDER);
        names.add(HISTORY);
    }

    ///////////////////////////////////////////////////////////////////////////
    // Protected Methods
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Package / Private Methods
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Inner Classes
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Initialization
    ///////////////////////////////////////////////////////////////////////////

}
